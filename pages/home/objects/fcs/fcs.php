                <span id="home_fcs_lower_text_headline_lower">
                    New Lighting, Significant Savings
                </span>

            </div>
            <div id="home_fcs_lower_text_first_paragraph_container">
                <span id="home_fcs_lower_text_first_paragraph">
                    Brighter, clearer lighting is delivering dividends to the Idaho Department of
                    Corrections (IDOC), enabling employees to see remarkable detail on security cameras
                    and enhancing visibility in the yard.<br><br>

                    Utilizing existing fixtures, Global Energy & Lighting’s 375W CeramaTek kits were
                    installed atop 50’ poles in the yard area, replacing older 1000-watt high pressure
                    sodium (HPS) technology. Dim and yellow-hued lighting has been replaced with clean,
                    bright, white light, with excellent clarity that is much appreciated by the prison staff.<br><br>

                    Michael McCabe, construction manager for IDOC, saw an immediate difference in
                    clarity on the security cameras as well as from guard posts in the yard. With a
                    reduction of 63% in energy use, the new lighting has an aggressive ROI of only 2 years.
                    And with an expected lamp life of 50,000 hours, IDOC will realize tremendous savings
                    on maintenance costs over the aging HPS system’s 20,000 hour life.<br><br>
                </span>
