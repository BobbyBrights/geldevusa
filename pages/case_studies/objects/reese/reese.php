            A Hershey, PA manufacturing and distribution facility has implemented Global Energy & Lighting’s CeramaTek®
            solutions to improve safety on its roadways and security in its loading areas.     <br><br>

            Facility management chose to retrofit existing systems with Global’s CeramaTek® kits to manage the company’s
            lighting needs. The majority of the project centered on replacing the 400-watt high-pressure sodium (HPS)
            system with 210-watt CeramaTek®® kits. The HPS lamps rated color rendering index (CRI, a measure of a light
            source’s fidelity to natural light) of 24 was easily outmatched by the CMH’s rating of 92. Additionally,
            the ceramic metal-halides (CMHs) outperform HPS in terms of life hours, boasting a 50,000-hour rated life
            over 24,000. The location will be experiencing truer, cleaner light for longer, resulting in increased
            performance for video surveillance as well as acting as a deterrent.   <br><br>

