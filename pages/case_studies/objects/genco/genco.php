            A manufacturing and distribution facility in Ogden, UT is enjoying the many benefits of Global Energy &
            Lighting’s retrofit solutions. The 550,000 square-foot site required an exterior lighting upgrade for
            several high-pressure sodium (HPS) lamps and ballasts. The new system was required to provide increased
            security and safety for the 24-hour operations of the center.<br><br>

            Facility management opted to retrofit existing wall pack and parking lot lighting with Global’s ceramic
            metal-halides (CMH) 45-, 60-, 210-, and 375-watt CeramaTek® kits to manage the company’s lighting needs.
            The majority of the project centered on replacing the 400-watt HPS system with 210-watt CeramaTek®® kits.
            The HPS lamp’s rated color rendering index (CRI, a measure of a light source’s fidelity to natural light)
            of 22 was easily outmatched by the CMH’s rating of 92. Additionally, the CMHs outperform HPS in terms of
            life hours, boasting a 50,000-hour rated life over 24,000. The location will be
            experiencing truer, cleaner light for longer, resulting in increased performance for video surveillance as
            well as acting as a deterrent.<br><br>

