            (HPS) system. The dim yellow-orange glow was refreshed with clean, bright light, illuminating illuminating
            area aesthetics as they were originally conceived. The new system provides 107 lm/W over the dismal 85 of
            the old scheme, allowing improved visibility for pedestrians, cyclists, and drivers.  <br><br>

