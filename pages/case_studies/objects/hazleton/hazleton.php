            A Hazleton, PA manufacturing and distribution facility recently implemented an energy efficiency upgrade to
            its exterior lighting. The large perimeter of the structure required improved illumination over the existing
            high-pressure sodium (HPS) system. High visibility was required to ensure security in areas such as facility
            entrances and loading dock spaces. <br><br>

            Facility management chose to retrofit existing wall pack and parking lot lighting with Global’s ceramic
            metal-halide (CMH) CeramaTek® kits to manage the company’s lighting needs. The majority of the project
            centered on replacing the 400-watt HPS system with 210-watt CeramaTek® kits. The HPS lamps rated color
            rendering index (CRI, a measure of a light source’s fidelity to natural light) of 24 was easily outmatched
            by the CMHs rating of 92. Additionally, the CMHs outperform HPS in terms of life hours, boasting a
            50,000-hour rated life over 24,000. Current metrics indicate the location will be experiencing truer,
            cleaner light for longer, resulting in increased performance for video surveillance as well as acting as a
            deterrent.<br><br>

